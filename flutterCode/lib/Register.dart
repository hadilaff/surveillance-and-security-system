  //@override
  void _registerUser(BuildContext context) async {
    try {
      // Récupérez les valeurs des champs de texte
      String username = usernameController.text;
      String email = emailController.text;
      String password = passwordController.text;
      String confirmPassword = confirmPasswordController.text;

      if (password != confirmPassword) {
        // Gérez le cas où les mots de passe ne correspondent pas
        print("Passwords do not match");
        return;
      }

      // Créez un nouvel utilisateur avec l'email et le mot de passe
      UserCredential userCredential =
          await FirebaseAuth.instance.createUserWithEmailAndPassword(
        email: email,
        password: password,
      );

      // Mettez à jour le profil de l'utilisateur avec le nom d'utilisateur
      await userCredential.user?.updateDisplayName(username);

      // Naviguez vers la page suivante après l'enregistrement
      Navigator.push(
        context,
        MaterialPageRoute(builder: (context) => LoginPage()),
      );
    } catch (e) {
      // Gérez les erreurs d'enregistrement ici
      print('Registration failed: $e');
    }
  }

